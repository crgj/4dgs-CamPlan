export type LanguageKey = keyof typeof translations.zh;

export function useTranslation() {
  const locale = usePlanner((s) => s.locale ?? 'zh');
  const t = (key: LanguageKey): string => {
    return translations[locale][key] || key;
  };
  return { t, locale };
}
